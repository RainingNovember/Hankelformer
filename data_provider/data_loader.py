class Dataset_GeneExp(Dataset):
    def __init__(self, root_path, flag='train', size=None,
                 features='M', data_path='circadian_geneexp.csv',
                 target='OT', scale=True, timeenc=0, freq='h'):
        # size [seq_len, label_len, pred_len]
        if size == None:
            self.seq_len = 5  # 输入5个时间点
            self.label_len = 2  # 重叠部分
            self.pred_len = 3  # 预测未来3个时间点
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        
        # 初始化
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]
        
        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq
        
        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()
        
    def __read_data__(self):
        # 读取数据
        df_raw = pd.read_csv(os.path.join(self.root_path, self.data_path), header=0)
        
        # 数据预处理：检查并处理NaN值
        if df_raw.isnull().any().any():
            print("Warning: NaN values found in data. Filling with 0.")
            df_raw = df_raw.fillna(0)
            
        # 检查极值并打印统计信息
        print(f"Data shape: {df_raw.shape}")
        print(f"Min value: {df_raw.values.min()}, Max value: {df_raw.values.max()}")
        
        # 基因名称作为列
        gene_names = df_raw.columns.tolist()
        
        # 添加日期列
        num_timepoints = len(df_raw)
        df_raw['date'] = pd.date_range(start='2023-01-01', periods=num_timepoints, freq='h')
        
        # 数据划分策略
        num_samples = num_timepoints - self.seq_len - self.pred_len + 1
        num_train = int(num_samples * 0.7)
        num_test = int(num_samples * 0.2)
        num_vali = num_samples - num_train - num_test
        
        border1s = [0, num_train, num_train + num_vali]
        border2s = [num_train, num_train + num_vali, num_samples]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]
        
        # 处理特征
        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns.tolist()
            cols_data.remove('date')
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            target_gene = self.target if self.target and self.target in df_raw.columns else gene_names[0]
            df_data = df_raw[[target_gene]]
        
        # 安全的数据标准化
        self.scaler = StandardScaler()
        data = df_data.values
        
        if self.scale:
            # 确保训练集有足够的数据进行拟合
            train_data = data[border1s[0]:max(border1s[0]+2, border2s[0])]
            # 计算数据统计信息前检查
            if np.isnan(train_data).any() or np.isinf(train_data).any():
                print("Warning: NaN or Inf values in training data! Replacing with zeros.")
                train_data = np.nan_to_num(train_data, nan=0.0, posinf=0.0, neginf=0.0)
            
            # 使用安全的标准化方法
            self.scaler.fit(train_data)
            # 检查标准化是否正确进行
            if np.isnan(self.scaler.mean_).any() or np.isnan(self.scaler.scale_).any():
                print("Warning: NaN in scaler parameters! Using robust standardization.")
                # 使用更健壮的标准化方法
                self.scaler.mean_ = np.nanmean(train_data, axis=0)
                self.scaler.scale_ = np.nanstd(train_data, axis=0) + 1e-8  # 添加小常数避免除零
            
            data = self.scaler.transform(data)
        
        # 检查转换后数据
        if np.isnan(data).any() or np.isinf(data).any():
            print("Warning: NaN or Inf in transformed data! Replacing with zeros.")
            data = np.nan_to_num(data, nan=0.0, posinf=0.0, neginf=0.0)
            
        # 创建时间特征
        df_stamp = df_raw[['date']]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        
        # 简化时间特征处理 - 使用恒定值避免时间特征引起的问题
        data_stamp = np.zeros((len(df_stamp), 4))  # 提供简单时间特征
            
        # 构建序列数据集
        data_x = np.zeros((num_samples, self.seq_len, data.shape[1]))
        data_y = np.zeros((num_samples, self.label_len + self.pred_len, data.shape[1]))
        data_stamp_x = np.zeros((num_samples, self.seq_len, data_stamp.shape[1]))
        data_stamp_y = np.zeros((num_samples, self.label_len + self.pred_len, data_stamp.shape[1]))
        
        # 填充数据
        for i in range(num_samples):
            s_begin = i
            s_end = s_begin + self.seq_len
            r_begin = s_end - self.label_len
            r_end = r_begin + self.label_len + self.pred_len
            
            data_x[i] = data[s_begin:s_end]
            data_y[i] = data[r_begin:r_end]
            data_stamp_x[i] = data_stamp[s_begin:s_end]
            data_stamp_y[i] = data_stamp[r_begin:r_end]
        
        self.data_x = data_x[border1:border2]
        self.data_y = data_y[border1:border2]
        self.data_stamp_x = data_stamp_x[border1:border2]
        self.data_stamp_y = data_stamp_y[border1:border2]
        
        # 修改为
        flag_type = {0: 'train', 1: 'val', 2: 'test'}
        print(f"{flag_type[self.set_type]} {len(self.data_x)}")
    
    def __getitem__(self, index):
        return (
            self.data_x[index], 
            self.data_y[index], 
            self.data_stamp_x[index], 
            self.data_stamp_y[index]
        )
    
    def __len__(self):
        return len(self.data_x)
    
    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)










